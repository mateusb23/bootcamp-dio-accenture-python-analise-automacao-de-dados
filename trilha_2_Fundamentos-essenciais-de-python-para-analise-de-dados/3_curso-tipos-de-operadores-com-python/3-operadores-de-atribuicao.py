## OPERADORES DE ATRIBUIÇÃO
# Operadores de atribuição são usados para atribuir valores a variáveis. Eles incluem:
# - Atribuição simples (=)
# - Atribuição composta (+=, -=, *=, /=, //=, %=, **=)
# Exemplo de uso dos operadores de atribuição
# Atribuição simples
a = 10
print("Valor de a:", a)  # 10
# Atribuição composta
a += 5  # a = a + 5
print("Valor de a após += 5:", a)  # 15
a -= 3  # a = a - 3
print("Valor de a após -= 3:", a)  # 12
a *= 2  # a = a * 2
print("Valor de a após *= 2:", a)  # 24
a /= 4  # a = a / 4
print("Valor de a após /= 4:", a)  # 6.0
a //= 2  # a = a // 2
print("Valor de a após //= 2:", a)  # 3.0
a %= 2  # a = a % 2
print("Valor de a após %= 2:", a)  # 1.0
a **= 3  # a = a ** 3
print("Valor de a após **= 3:", a)  # 1.0

