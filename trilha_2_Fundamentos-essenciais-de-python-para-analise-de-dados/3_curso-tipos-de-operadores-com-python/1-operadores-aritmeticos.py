## OPERADORES ARITMÉTICOS
# Operadores aritméticos são usados para realizar operações matemáticas em Python. Eles incluem:
# - Adição (+)
# - Subtração (-)
# - Multiplicação (*)
# - Divisão (/)
# - Divisão inteira (//)
# - Módulo (%)
# - Exponenciação (**)
# Exemplo de uso dos operadores aritméticos
a = 10
b = 3
print("Adição:", a + b)          # 13
print("Subtração:", a - b)       # 7
print("Multiplicação:", a * b)    # 30
print("Divisão:", a / b)          # 3.333333333333333
print("Divisão inteira:", a // b) # 3
print("Módulo:", a % b)           # 1
print("Exponenciação:", a ** b)   # 1000
