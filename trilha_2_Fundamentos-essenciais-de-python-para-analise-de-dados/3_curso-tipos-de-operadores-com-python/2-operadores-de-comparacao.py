## OPERADORES DE COMPARAÇÃO
# Operadores de comparação são usados para comparar valores e retornar um resultado booleano (True ou False). Eles incluem:
# - Igual a (==)
# - Diferente de (!=)
# - Maior que (>)
# - Menor que (<)
# - Maior ou igual a (>=)
# - Menor ou igual a (<=)
# Exemplo de uso dos operadores de comparação
x = 5
y = 10
print("x é igual a y:", x == y)         # False
print("x é diferente de y:", x != y)     # True
print("x é maior que y:", x > y)        # False
print("x é menor que y:", x < y)        # True
print("x é maior ou igual a y:", x >= y) # False
print("x é menor ou igual a y:", x <= y) # True

