# CONVERTENDO TIPOS DE DADOS

# CONVERTENDO string(str) PARA INTEIRO
numero_str = "10"
numero_int = int(numero_str)
print(numero_int)  # Saída: 10

# CONVERTENDO string(str) PARA PONTO FLUTUANTE
numero_str = "3.14"
numero_float = float(numero_str)
print(numero_float)  # Saída: 3.14

# CONVERTENDO INTEIRO PARA string
numero_int = 10
numero_str = str(numero_int)
print(numero_str)  # Saída: "10"

# CONVERTENDO PONTO FLUTUANTE PARA string
numero_float = 3.14
numero_str = str(numero_float)
print(numero_str)  # Saída: "3.14"
