# existe uma conveção para nomear variáveis, que é a seguinte:
# - letras minúsculas
# - palavras separadas por underline
# - não pode começar com número
# - não pode conter caracteres especiais
# - nomes susgestivos
# exemplo de variáveis
nome = "João"
idade = 30
altura = 1.75

# e existe uma conveção para nomear constantes, que é a seguinte:
# - letras maiúsculas
# - palavras separadas por underline
# - não pode começar com número
# - não pode conter caracteres especiais
# - nomes susgestivos
# exemplo de constantes
PI = 3.14
GRAVIDADE = 9.81
VELOCIDADE_DA_LUZ = 299792458

